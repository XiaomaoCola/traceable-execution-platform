"""
审计模块（Audit logging module for tamper-proof event tracking）

本模块用于记录系统中的关键行为审计日志（Audit Log），
其设计目标、约束条件和用途，与普通业务日志（logging）有本质区别。

为什么需要单独的审计系统，而不是直接复用 logging：

1. 设计目标不同
   - 普通 logging 主要用于调试、排障、运维观测和性能分析，
     日志级别、格式、输出位置和保留周期通常是可变的。
   - 审计日志用于合规、追责、取证和争议处理，
     其核心目标是“证明某个行为在某个时间确实发生过”。

2. 不可变性要求不同
   - 普通日志可以轮转、压缩、采样甚至删除。
   - 审计日志必须遵循 append-only 原则，只能追加、不能修改或回滚，
     以保证历史记录的完整性和可信度。

3. 生命周期与权限不同
   - 普通日志通常只保留较短时间（如 7～30 天），并对运维或开发可见。
   - 审计日志往往需要长期保存，并受到更严格的访问和管理权限控制。

4. 数据结构与语义不同
   - 普通日志以“系统行为”为中心（函数调用、耗时、异常等）。
   - 审计日志以“业务行为”为中心，明确记录：
     谁（actor）在什么时间（timestamp）
     对哪个资源（resource）做了什么操作（action）。

5. 对 logging 的依赖是有意隔离的
   - 本模块不依赖 logging 的 handler、level 或配置来保证审计日志的落盘，
     避免因配置变更、日志级别调整或输出关闭而丢失审计记录。
   - logging 仅作为旁路信号或未来集成统一日志系统的扩展点。

设计结论：
审计日志与普通业务日志在职责、可靠性要求和合规属性上均不同，
因此采用独立的审计模块与存储机制，是一种有意识的架构选择，
而非重复造轮子。
"""
